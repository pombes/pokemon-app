export type TKey = keyof typeof nl;

const dict: Record<Lang, Record<TKey, string>> = { nl, en };

export function t(
  lang: Lang,
  key: TKey,
  vars?: Record<string, string | number>
): string {
  let s = dict[lang]?.[key] ?? dict.nl[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(`{${k}}`, String(v));
    }
  }
  return s;
}
